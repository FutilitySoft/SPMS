<h2 align='center'>Bravo Fleet Intrepid Class Specifications</h2>
<div align='center'><img src='images/specs/Intrepid_Anim.gif' border='0' /></div>

<p>In 2370 a new class of ship entered service in Starfleet. The ship would be, when commissioned, the fastest class of starship in service in any fleet. She would be a frigate/light explorer/scout ship. Well armed, maneuverable, well protected, and posses a sensor suite comparable to the Galaxy Class'. The ship features a unique warp nacelle design, which allows the nacelles to pivot up when in warp flight, improving the warp field. The class was also the first to use bioneural gel packs in the computer systems.<p>


<div align='center'><img src='images/ship/voyneb2.jpg' /></div>

<p>Starfleet's shipbuilding policy of the last century has emphasized ever larger, more capable designs capable of carrying out a wide variety of missions within a single hull, and many times simultaneously. However, this has led to a gradual fall in the numbers of vessels which can be fielded - where the Excelsior Class numbers well over a hundred, the Ambassadors were limited to a production run of twenty six, and the Galaxy Class to six ships initially, fourteen ships at present. While the exploration capacity of these Classes is still sufficient to meet the tasks demanded of them, the lack of numbers has led to a certain restriction in flexibility. Losses among the Galaxy Class have also emphasized that placing "all the eggs in one basket" is not necessarily the wisest choice.</p>

<p>The Intrepid design was fielded partly to correct this situation. The design requirement, issued in 2358, called for a vessel in the 360 meter range with a volume some 15% that of a Galaxy Class. Although the limited size meant that the vessel would have much lower overall capabilities than the Galaxy Class, this was to be in some part offset by the use of the latest technology. The ships computer system would be the most advanced model in service; the increased automation this afforded combined with a new generation of sensor equipment would allow the Intrepid Class almost 70% of the scanning and analysis capabilities of a Galaxy Class starship. A top cruise speed some three times that of the Galaxy would further enhance the vessels exploration capabilities.</p>

<p>Diplomatic functions would be more problematic - hosting such functions requires supporting large numbers of extra personnel, and the Intrepid Class simply would not have the room for this. Her diplomatic capabilities are therefore limited to relatively small events generally involving Federation negotiations with a single party.

<p> The Intrepid's combat role was also somewhat limited by her size; unable to carry large numbers of torpedoes or high power phaser banks, Starfleet settled on a scouting and support mission for the ship. Fitted out with type-VIII phaser arrays and single fire torpedo tubes standard. The Intrepid would be capable of engaging vessels the size of a K'T'inga Class Battle Cruiser or Galor Class Destroyer one-on-one. Her high speed and maneuverability combined with the advanced sensor system would also make her a perfect platform to conduct long range reconnaissance missions ahead of fleet operations. Within a major fleet battle the Intrepid would also act in support of larger ships, harrying enemy fleet units and drawing fire away from larger Federation ships. </p>

<p> Development of the intrepid Class went relatively smoothly in the early and mid stages, but a major problem arose in 2370 with the discovery that the energy emissions of standard warp drives caused considerable cumulative damage to the structure of subspace. Starfleet called for all designs then in progress to be modified to eliminate this effect, and it was decided to use the Intrepid Class as a test bed for this technology. After some experimentation it was found that the effect could be avoided by using a warp field with a much elongated Z-axis, combined with complex warp field modulation and the utilization of new materials during the manufacture of warp coils. This has led to the ships saucer section having the forward ellipse now becoming common on Federation designs. The new warp field configuration did lead to a 15% reduction in the speed of the Intrepid Class across all flight regimes, bringing the top cruise speed down from Warp 9.99 to Warp 9.975, and the maximum rated speed from Warp 9.992 to Warp 9.985. </p>

<p> The program suffered a serious setback when the USS Voyager, second in the Class, was apparently lost on her first mission. Building was suspended on the Class while the designs were subjected to intense scrutiny; after a year without results the Intrepid project was resumed. It has since been discovered that Voyager was in fact displaced to the other side of the Galaxy, in the Delta Quadrent, by an alien technology, from where she is now attempting to make her way home. </p>

<p> One feature unique to the Intrepid Class among Federation Starships of this size is the ability to land on a planetary surface. This feature has been incorporated after several incidents in which transporters and shuttle craft have been unable to operate through exotic environmental conditions during evacuations or other surface related operations. </p>
  
